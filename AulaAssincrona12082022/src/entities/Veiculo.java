package entities;

public class Veiculo {


	private String tipoVeiculo;
	private String marca;
	private String ano;
	private String modelo;
	private int quantidadePneus;
	private String potencia;
	private char tipoCombustivel;
	private int quantosPassageiros;
	private double valor;

		
	public String getTipoVeiculo() {
		return tipoVeiculo;
	}
	public static void setTipoVeiculo(String tipoVeiculo) {
		this.tipoVeiculo = tipoVeiculo;
	}
	public int getMarca() {
		return marca;
	}
	public static void setMarca(String marca) {
		this.marca = marca;
	}
	public String getAno() {
		return ano;
	}
	public static void setAno(int ano) {
		this.ano = ano;
	}
	public String getModelo() {
		return modelo;
	}
	public static void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getQuantidadePneus() {
		return quantidadePneus;
	}
	public static void setQuantidadePneus(String QuantidadePneus) {
		this.quantidadePneus = QuantidadePneus;
	}
	public String getPotencia() {
		return potencia;
	}
	public static void setPotencia(String potencia) {
		this.potencia = potencia;
	}
	public char getTipoCombustivel() {
		return tipoCombustivel;
	}
	public static void setTipoCombustivel(String TipoCombustivel) {
		this.tipoCombustivel = TipoCombustivel;
	}
	
	public int getQuantosPassageiros() {
		return quantosPassageiros;
	}
	public static void setQuantosPassageiros(String QuantosPassageiros) {
		this.quantosPassageiros = QuantosPassageiros;
	}
	public double getValor() {
		return valor;
	}
	public static void setValor(String Valor) {
		this.valor = Valor;
	}
	

}


	

